    restrict_room_creation = false
    -- Rooms survive being empty. Otherwise Prosody destroys them on the
    -- last leave and the MAM archive goes with them.
    muc_room_default_persistent = true
    muc_room_default_public      = true
    muc_room_default_members_only = false
    muc_room_default_change_subject = false
    muc_room_default_allow_member_invites = true
    muc_room_default_public_jids = true
    -- Force archiving on every room regardless of per-room config — saves
    -- us having to send a config form after creation.
    muc_log_all_rooms = true
    muc_log_by_default = true
