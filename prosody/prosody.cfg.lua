-- Authentication backend.
--
-- Session 09 (ADR-007): dynamic auth via HTTP callback to the Go API. The
-- web client mints a short-lived token through /api/xmpp/token and uses it
-- as the XMPP password; Prosody forwards (user, pass) to /internal/xmpp/auth
-- which validates and consumes the token.
--
-- Falls back to `internal_hashed` if KROVARA_XMPP_AUTH_URL is unset so
-- `prosodyctl adduser` (session 08 smoke flow) keeps working.
local auth_url = os.getenv("KROVARA_XMPP_AUTH_URL")
if auth_url and auth_url ~= "" then
    authentication = "http"
    http_auth_url = auth_url
else
    authentication = "internal_hashed"
end

