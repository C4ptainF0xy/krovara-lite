    -- until the owner submits a config form. With our lazy-creation flow
    -- nobody ever does, so the room stays locked and everyone but the
    -- creator is bounced. Disable the lock — defaults are already what
    -- we want.
    muc_room_locking = false

Component "pubsub.krovara.local" "pubsub"

-- Bot components.
--
-- Per-bot Lua snippets are written by the Krovara API into
-- /etc/prosody/components.d/ (one `bot-<short>.cfg.lua` per bot, see
-- internal/bots). We iterate that dir on every startup / reload and
-- dofile each file. A stale snippet pointing at a deleted bot is
-- harmless beyond a warning.
--
-- The legacy single-file path `/etc/prosody/components.cfg.lua` is still
-- supported for ops who manage Prosody config by hand.
local lfs_ok, lfs = pcall(require, "lfs")
if lfs_ok then
    local dir = "/etc/prosody/components.d/"
    if lfs.attributes(dir, "mode") == "directory" then
        for f in lfs.dir(dir) do
            if f:sub(-#".cfg.lua") == ".cfg.lua" then
                pcall(dofile, dir .. f)
            end
        end
    end
end
local has_legacy, _ = pcall(dofile, "/etc/prosody/components.cfg.lua")
_ = has_legacy

